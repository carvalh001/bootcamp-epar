
export const formatSubmissionDate = (timestamp?: string): string => {
  if (!timestamp) return 'Data n√£o registrada';
  const date = new Date(timestamp);
  return date.toLocaleString('pt-BR', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit'
  });
};
